package rest;

public class StartMessage {
    private Integer numberOfColors;
    private Integer numberOfTries;
    private Integer numberOfCodes;

    public Integer getNumberOfColors() {
        return numberOfColors;
    }

    public void setNumberOfColors(Integer numOfColors) {
        this.numberOfColors = numOfColors;
    }

    public Integer getNumberOfTries() {
        return numberOfTries;
    }

    public void setNumberOfTries(Integer numOfTries) {
        this.numberOfTries = numOfTries;
    }

    public Integer getNumberOfCodes() {
        return numberOfCodes;
    }

    public void setNumberOfCodes(Integer numOfCodes) {
        this.numberOfCodes = numOfCodes;
    }
}
